void	ft_del_elem(char **arr, char symb)
{
	int i;
	int j;

	i = 0;
	while (arr[i])
	{
		j = 0;
		while (arr[i][j] != '\0')
		{
			if (arr[i][j] == symb)
				arr[i][j] = '.';
			j++;
		}
		i++;
	}
}
