int		ft_check_z1(char *buf, int i)
{
	if (((i + 7 < 21) && (buf[i + 1] == '#' && buf[i + 6] == '#' &&
	buf[i + 7] == '#')) || ((i + 9 < 21) && (buf[i + 5] == '#' &&
	buf[i + 4] == '#' && buf[i + 9] == '#')))
		return (1);
	return (0);
}

int		ft_check_z2(char *buf, int i)
{
	if (((i + 5 < 21) && (buf[i + 1] == '#' && buf[i + 5] == '#' &&
	buf[i + 4] == '#')) || ((i + 11 < 21) && (buf[i + 5] == '#' &&
	buf[i + 6] == '#' && buf[i + 11] == '#')))
		return (1);
	return (0);
}
